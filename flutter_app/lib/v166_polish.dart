String friendlyErrorMessage(Object error, [String lang = 'ar']) {
  final raw = error.toString().replaceFirst('ApiException: ', '').replaceFirst('Exception: ', '');
  final lower = raw.toLowerCase();
  if (lang != 'ar') {
    if (lower.contains('timeout')) return 'The server took too long to respond. Check your connection and try again.';
    if (lower.contains('socket') || lower.contains('network')) return 'Could not reach the server. Check your internet connection.';
    if (lower.contains('microphone') || lower.contains('permission')) return 'Microphone permission is required for voice rooms.';
    return raw.isEmpty ? 'An unexpected error occurred. Please try again.' : raw;
  }
  if (lower.contains('timeout')) return 'تأخر الخادم في الاستجابة. تحقق من الإنترنت ثم أعد المحاولة.';
  if (lower.contains('socket') || lower.contains('network') || lower.contains('connection')) return 'تعذر الوصول إلى الخادم. تحقق من اتصال الإنترنت وعنوان الخادم.';
  if (lower.contains('microphone') || lower.contains('permission')) return 'الغرفة الصوتية تحتاج السماح للميكروفون من إعدادات الهاتف.';
  if (lower.contains('401') || lower.contains('unauthorized')) return 'انتهت جلسة الدخول. سجّل الدخول من جديد.';
  return raw.isEmpty ? 'حدث خطأ غير متوقع. أعد المحاولة.' : raw;
}

